#!/usr/bin/env node
/**
 * combine.js — Reads every courses/<slug>/data.json and writes site/data.js.
 *
 * Each course lives in its own directory under site/courses/<slug>/data.json.
 * This script reads every course in alphabetical order and writes the COURSES
 * global used by the browser.  It is the only build step the site needs.
 *
 * Run:  node site/combine.js
 * Vercel buildCommand: node site/combine.js
 */

const fs   = require('fs');
const path = require('path');

const COURSES_DIR = path.join(__dirname, 'courses');
const OUTPUT_PATH = path.join(__dirname, 'data.js');

if (!fs.existsSync(COURSES_DIR)) {
  console.error('❌  No site/courses/ directory found. Create at least one course first.');
  process.exit(1);
}

const slugs = fs.readdirSync(COURSES_DIR)
  .filter(d => {
    const dataFile = path.join(COURSES_DIR, d, 'data.json');
    return fs.statSync(path.join(COURSES_DIR, d)).isDirectory() && fs.existsSync(dataFile);
  })
  .sort();

if (!slugs.length) {
  console.error('❌  No courses/*/data.json files found.');
  process.exit(1);
}

const courses = slugs.map(slug => {
  const raw  = fs.readFileSync(path.join(COURSES_DIR, slug, 'data.json'), 'utf8');
  const data = JSON.parse(raw);

  // Ensure every lesson that has a URL also has a course-scoped path field.
  // This lets lesson.html build correct ?path= params without guessing.
  for (const phase of (data.phases || [])) {
    for (const lesson of (phase.lessons || [])) {
      if (lesson.url && !lesson.path) {
        const m = lesson.url.match(/phases\/([^/]+)\/([^/]+)\/?$/);
        if (m) lesson.path = `phases/${data.slug}/${m[1]}/${m[2]}`;
      }
    }
  }

  return data;
});

const totalLessons  = courses.reduce((a, c) => a + (c.phases || []).reduce((b, p) => b + (p.lessons || []).length, 0), 0);
const totalGlossary = courses.reduce((a, c) => a + (c.glossary || []).length, 0);

const output = `// Auto-generated by combine.js — do not edit manually.
// Last built: ${new Date().toISOString()}
// ${courses.length} course(s) · ${totalLessons} lessons · ${totalGlossary} glossary terms

const COURSES = ${JSON.stringify(courses, null, 2)};
`;

fs.writeFileSync(OUTPUT_PATH, output, 'utf8');
console.log(`✅  data.js — ${courses.length} course(s), ${totalLessons} lessons, ${totalGlossary} glossary terms`);
for (const c of courses) {
  const l = (c.phases || []).reduce((a, p) => a + (p.lessons || []).length, 0);
  console.log(`    ${c.slug}: ${(c.phases || []).length} phases · ${l} lessons · ${(c.glossary || []).length} glossary terms`);
}
